        self.control_end : int = control_end
        self.control_codes : Dict[int, Tuple[str, str, Optional[Tuple[Callable[[int], str]]]]] = control_codes
        self.control_header : Tuple[str, str, Optional[Tuple[Callable[[int], str]]]] = control_header
